package org.apache.lucene.util;

/**
 * Flat lookup table for Unicode simple case folding of BMP codepoints.
 * Generated using ICU4J v${icuVersion}, unicode version: ${unicodeVersion}.
 */
final class FoldTable {
  private FoldTable() {}

  static final char[] TABLE = new char[0x10000];

  static {
    for (int i = 0; i < TABLE.length; i++) {
      TABLE[i] = (char) i;
    }
    int[] mappings = {${generateFoldMappings()}
    };
    for (int i = 0; i < mappings.length; i += 2) {
      TABLE[mappings[i]] = (char) mappings[i + 1];
    }
  }
}
