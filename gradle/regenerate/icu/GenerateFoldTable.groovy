import com.ibm.icu.lang.UCharacter
import com.ibm.icu.lang.UScript
import com.ibm.icu.util.VersionInfo
import java.nio.file.Paths
import java.util.Locale

def icuVersion = VersionInfo.ICU_VERSION.toString()
def unicodeVersion = UCharacter.getUnicodeVersion().toString()

def outputFile = Paths.get(args[0])

def generateFoldMappings() {
  StringBuilder sb = new StringBuilder()
  int lastScript = -1
  int count = 0

  for (int c = 0; c < 0x10000; c++) {
    int folded = UCharacter.foldCase(c, true)
    if (folded != c && folded >= 0 && folded <= 0xFFFF) {
      int script = UScript.getScript(c)
      String scriptName = UScript.getName(script)

      if (script != lastScript) {
        if (count > 0) {
          sb.append("\n")
        }
        sb.append("\n    // ${scriptName}")
        lastScript = script
      }
      String fromChar = new String(Character.toChars(c))
      String toChar = new String(Character.toChars(folded))
      sb.append(String.format(Locale.ROOT, "\n    0x%04X, 0x%04X, // %s -> %s", c, folded, fromChar, toChar))
      count++
    }
  }
  return sb.toString()
}

